package Ex_03;

public class Animal {

    private String nome, especie, pais;
    private double peso;
    private String[] alimentacao;

    public Animal(String nome, String especie, String pais, double peso, String[] alimentacao) {
        this.nome = nome;
        this.especie = especie;
        this.pais = pais;
        this.peso = peso;
        this.alimentacao = alimentacao;
    }


}
